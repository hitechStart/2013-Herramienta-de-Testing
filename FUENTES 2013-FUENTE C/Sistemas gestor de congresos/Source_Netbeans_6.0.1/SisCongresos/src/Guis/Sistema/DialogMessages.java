    /** Muestra un mensaje
     * @param mess El texto del mensaje
     * @param type El tipo de mensaje donde segun los valores de las constantes
     *   ERROR_MESS para error, INF_MESS para informacion, QUES_MESS para pregunta o peticion;
     */
    public void setMessage(String mess,int type){
        lblTitle.setText(type==1?"! ! !":(type==2?"- O -":"? ? ?"));
        if(type==1){
            pnlAll.setBorder(new javax.swing.border.LineBorder(new java.awt.Color(255, 160, 112), 3, true));
            lblTitle.setForeground(Color.RED);
        }
        else if(type==2){
            pnlAll.setBorder(new javax.swing.border.LineBorder(Color.BLUE, 3, true));
            lblTitle.setForeground(Color.BLUE);
        }
        else if(type==3){
            pnlAll.setBorder(new javax.swing.border.LineBorder(Color.GREEN, 3, true));
            lblTitle.setForeground(Color.GREEN);
        }
        lblMessage.setText(mess); validateTree(); pack();
        setLocationRelativeTo(null); setVisible(true);
    }
    
    
